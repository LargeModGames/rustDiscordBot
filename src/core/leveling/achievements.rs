use serde::{Deserialize, Serialize};

#[derive(Debug, Clone, Serialize, Deserialize)]
pub struct Achievement {
    pub id: String,
    pub name: String,
    pub description: String,
    pub emoji: String,
    pub category: AchievementCategory,
    pub reward_xp: u64,
}

#[derive(Debug, Clone, Serialize, Deserialize, PartialEq, Eq)]
pub enum AchievementCategory {
    Levels,
    Activity,
    Dedication,
    Progression,
    Special,
    Competition,
    Content,
    Meta,
    Community,
}

impl AchievementCategory {
    pub fn title(&self) -> &str {
        match self {
            AchievementCategory::Levels => "Levels",
            AchievementCategory::Activity => "Activity",
            AchievementCategory::Dedication => "Dedication",
            AchievementCategory::Progression => "Progression",
            AchievementCategory::Special => "Special",
            AchievementCategory::Competition => "Competition",
            AchievementCategory::Content => "Content",
            AchievementCategory::Meta => "Meta",
            AchievementCategory::Community => "Community",
        }
    }
}

